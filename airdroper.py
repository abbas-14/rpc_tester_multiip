def createTxn(web3, rcvAddr, i):
    txn = {
        'to': rcvAddr,
        'value': web3.to_wei(AIRDROP_AMT, 'ether'),
        'gas': 21000,
        'gasPrice': (web3.eth.gas_price + 1000 * i),
        'nonce': web3.eth.get_transaction_count(MAIN_ACC),
    }
    return txn

